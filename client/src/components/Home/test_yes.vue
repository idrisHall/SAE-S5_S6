<template>
<div class="slider">
    <div class="list" :style="{ transform: 'translateX(' + leftTransform + 'px)' }">
        <div class="item" v-for="(item, index) in items" :key="index" :class="{ active: index === active }">
            <img :src="item.image">
        </div>
    </div>
    <div class="circle"></div>
    <div class="content">
        <div>Nos</div>
        <div>Plats</div>
        <button>See More</button>
    </div>
    <div class="arow">
        <button @click="prev" id="prev">←</button>
        <button @click="next" id="next">→</button>
    </div>
</div>
</template>

<script>
export default {
    data() {
        return {
            items: [{
                    image: 'img/plat_tunisien_sansbg.png'
                },
                {
                    image: 'img/plat_mexicain.png'
                },
                {
                    image: 'img/plat_indien.png'
                },
                {
                    image: 'img/plat_japonais.png'
                },
                {
                    image: 'img/plat_marocain.png'
                }
            ],
            active: 1,
            count: 5,
            widthItem: 0
        };
    },
    computed: {
        leftTransform() {
            return this.widthItem * (this.active - 1) * -1;
        }
    },
    methods: {
        next() {
            if (this.active < this.count - 1) {
                this.active++;
            }
        },
        prev() {
            if (this.active > 0) {
                this.active--;
            }
        }
    },
    mounted() {
        this.widthItem = this.$el.querySelector('.item').offsetWidth;
    }
};
</script>

